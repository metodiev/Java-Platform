package oop.pack;

public class GSM {

	//fields
	private String buttons;
	private String display;
	private String displaySize;
	private int batterry;
	
	
	//default constructor
	public GSM() {
		
	}
	/**
	 * @param buttons
	 * @param display
	 * @param displaySize
	 * @param batterry
	 */
	public GSM(String buttons, String display, String displaySize, int batterry) {
		super();
		this.buttons = buttons;
		this.display = display;
		this.displaySize = displaySize;
		this.batterry = batterry;
	}
	
	//constructor
	
	
	
}
